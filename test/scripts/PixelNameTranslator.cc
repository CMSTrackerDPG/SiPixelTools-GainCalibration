// ----------------------------------------------------------------------
int PixelNameTranslator::xdaq2detID(string sname) { 
  //  string sname = string(xdaqName); 
  // -- Remove the 'H' or 'F' in case it is present
  string::size_type letter = sname.find("H_MOD"); 
  if (letter != string::npos) {
    sname.erase(letter, 1); 
  } else {
    letter = sname.find("F_MOD"); 
    if (letter != string::npos) sname.erase(letter, 1); 
  }

  map<int, string>::iterator start = fDetID2Xdaq.begin();
  map<int, string>::iterator stop  = fDetID2Xdaq.end();
  for (map<int, string>::iterator imap = start; imap != stop; ++imap) {
    if (imap->second == sname) {
      return imap->first; 
    }
  }
  return -1; 
}

// ----------------------------------------------------------------------
string PixelNameTranslator::detID2xdaq(int detid) { 
  map<int, string>::iterator start = fDetID2Xdaq.begin();
  map<int, string>::iterator stop  = fDetID2Xdaq.end();
  for (map<int, string>::iterator imap = start; imap != stop; ++imap) {
    if (detid == imap->first) {
      return imap->second; 
    }
  }
  return "not found"; 
}

// ----------------------------------------------------------------------
int PixelNameTranslator::detID2Bpix(int detid, int &layer, int &ladder, int &module) {
  string sdetid = detID2xdaq(detid); 

  string::size_type bpix = sdetid.find("BPix"); 
  if (bpix == string::npos) {
    return 0; 
  }

  string sz = sdetid.substr(6, 1); 
  int    Sz = (sz=="p"?1:-1); 
  string sx = sdetid.substr(7, 1); 
  int    Sx = (sx=="I"?1:-1); 

  string::size_type pos1 = sdetid.find("_LYR") + 4; 
  string::size_type pos2 = sdetid.find("_LDR"); 
  string slayer = sdetid.substr(pos1, pos2-pos1); 
  int    Slayer(0); 
  istringstream stream0(slayer); stream0 >> Slayer; 

  pos1 = sdetid.find("_LDR") + 4; 
  pos2 = sdetid.find("_MOD"); 
  string sladder = sdetid.substr(pos1, pos2-pos1); 
  int    Sladder(0); 
  istringstream stream1(sladder); stream1 >> Sladder; 
  Sladder *= Sx;

  pos1 = sdetid.find("_MOD") + 4; 
  string smodule = sdetid.substr(pos1); 
  int    Smodule(0); 
  istringstream stream2(smodule); stream2 >> Smodule; 
  Smodule *= Sz;

  layer  = Slayer; 
  ladder = Sladder; 
  module = Smodule; 

  //   cout << sdetid  << " -> " << endl;
  //   cout << slayer  << " -> " << Slayer <<endl;
  //   cout << sladder << " -> " << Sladder <<endl;
  //   cout << smodule << " -> " << Smodule << endl;
  //   cout << sx      << " -> " << Sx << endl;
  //   cout << sz      << " -> " << Sz << endl;

  return 1; 

}


